def messages_to_turns(messages: list[dict]) -> list[dict]:
    """Group flat message list into user/agent turns."""
    turns = []
    agent_texts: list[str] = []
    tool_calls: list[str] = []
    current_user: str | None = None

    def flush():
        if current_user is not None:
            turns.append({
                "user":       current_user,
                "agent":      " ".join(filter(None, agent_texts)),
                "tool_calls": list(tool_calls),
            })
            agent_texts.clear()
            tool_calls.clear()

    for m in messages:
        role    = m.get("role", "")
        content = (m.get("content") or "").strip()
        tcs     = m.get("tool_calls") or []

        if role == "user":
            flush()
            current_user = content if not content.startswith("###") else None
        elif role == "assistant":
            if content:
                agent_texts.append(content)
            for tc in tcs:
                fn   = (tc.get("function") or tc) if isinstance(tc, dict) else {}
                name = fn.get("name", "")
                args = fn.get("arguments", "")
                if name:
                    tool_calls.append(f"{name}({str(args)[:80]})")

    flush()
    return [t for t in turns if t["user"]]


def split_violation_text(text: str) -> tuple[str, str]:
    """
    Split a policy_violation into (base_part, except_clause).
    The except_clause starts at 'Except for'.
    """
    m = re.search(r"(Except for .+)", text, re.DOTALL | re.IGNORECASE)
    if m:
        return text[:m.start()].rstrip(), m.group(1)
    return text, ""


def load_data(
    data_path: str,
    norms: dict,
    failed_only: bool,
    norm_filter: str | None,
) -> list[dict]:
    with open(data_path) as f:
        raw = json.load(f)

    tasks_by_id = {str(t["id"]): t for t in raw.get("tasks", [])}

    items = []
    for idx, sim in enumerate(raw.get("simulations", [])):
        task_id      = str(sim.get("task_id", ""))
        task         = tasks_by_id.get(task_id, {})
        ri           = sim.get("reward_info") or {}
        reward       = ri.get("reward")
        db_match     = (ri.get("db_check") or {}).get("db_match")
        violated_norm_id = sim.get("violated_norm")

        # Norm metadata (from norms file if provided)
        norm_meta = {}
        if violated_norm_id and violated_norm_id in norms:
            norm_meta = norms[violated_norm_id].get("metadata", {})

        # Split violation text for display
        policy_quote     = norm_meta.get("policy_quote", "")
        policy_violation = norm_meta.get("policy_violation", "")
        violation_base, violation_except = split_violation_text(policy_violation)

        # Task goal
        scenario = (task.get("user_scenario") or {}) if task else {}
        instr    = (scenario.get("instructions") or {}) if isinstance(scenario, dict) else {}
        goal     = instr.get("reason_for_call", "") if isinstance(instr, dict) else str(instr)
        persona  = instr.get("task_instructions", "") if isinstance(instr, dict) else ""
        known    = instr.get("known_info", "") if isinstance(instr, dict) else ""

        # Action checks
        action_checks = []
        for ac in (ri.get("action_checks") or []):
            a = ac.get("action") or {}
            action_checks.append({
                "name":    a.get("name", ""),
                "matched": ac.get("action_match", False),
                "type":    ac.get("tool_type", ""),
            })

        items.append({
            "idx":              idx,
            "sim_id":           str(sim.get("id", "")),
            "task_id":          task_id,
            "reward":           reward,
            "db_match":         db_match,
            "termination":      sim.get("termination_reason", ""),
            "goal":             goal,
            "persona":          persona,
            "known_info":       known,
            "turns":            messages_to_turns(sim.get("messages") or []),
            "action_checks":    action_checks,
            "violated_norm_id": violated_norm_id or "",
            "norm_description": norm_meta.get("description", ""),
            "norm_section":     norm_meta.get("policy_section", ""),
            "policy_quote":     policy_quote,
            "violation_base":   violation_base,
            "violation_except": violation_except,
        })

    if failed_only:
        items = [it for it in items if it["reward"] == 0]
    if norm_filter:
        items = [it for it in items if it["violated_norm_id"] == norm_filter]

    return items


