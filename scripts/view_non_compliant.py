# ---------------------------------------------------------------------------
# Routes
# ---------------------------------------------------------------------------

@app.route("/")
def index():
    return redirect(url_for("view_sim", pos=0))


@app.route("/view/<int:pos>")
def view_sim(pos):
    norm_filter = request.args.get("norm", "") or _state.get("norm_filter", "")
    items = _state["items_all"]

    if norm_filter:
        filtered = [it for it in items if it["violated_norm_id"] == norm_filter]
    elif _state.get("failed_only"):
        filtered = [it for it in items if it["reward"] == 0]
    else:
        filtered = items

    total = len(filtered)
    if total == 0:
        return f"<p style='color:white;padding:2em'>No items match the current filter.</p>", 404

    pos  = max(0, min(pos, total - 1))
    item = filtered[pos]

    return render_template_string(
        TEMPLATE,
        item         = item,
        pos          = pos,
        total        = total,
        current_norm = norm_filter,
        all_norm_ids = _state["all_norm_ids"],
    )


@app.route("/jump")
def jump():
    pos         = int(request.args.get("pos", 1)) - 1
    norm_filter = request.args.get("norm", "")
    return redirect(url_for("view_sim", pos=pos, norm=norm_filter or None))


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(
        description="Viewer for non-compliant agent traces.",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument(
        "--data", required=True,
        help="Path to the non-compliant traces JSON file.",
    )
    parser.add_argument(
        "--norms", default=None,
        help="Path to the norms JSON (adds descriptions and policy text to the UI).",
    )
    parser.add_argument(
        "--failed-only", action="store_true",
        help="Show only simulations where the agent failed (reward=0).",
    )
    parser.add_argument(
        "--norm", default=None,
        help="Filter to a specific violated norm ID, e.g. n6_confirm_before_return.",
    )
    parser.add_argument("--port", type=int, default=5010)
    args = parser.parse_args()

    # Load norms metadata
    norms: dict = {}
    if args.norms and os.path.isfile(args.norms):
        with open(args.norms) as f:
            norms = json.load(f)

    items = load_data(
        data_path   = args.data,
        norms       = norms,
        failed_only = args.failed_only,
        norm_filter = args.norm or "",
    )

    if not items:
        print("No items to display with the current filters.")
        return

    all_norm_ids = sorted({it["violated_norm_id"] for it in items if it["violated_norm_id"]})

    _state["items_all"]   = items
    _state["all_norm_ids"] = all_norm_ids
    _state["failed_only"] = args.failed_only
    _state["norm_filter"] = args.norm or ""

    n_pass = sum(1 for it in items if it["reward"] == 1)
    n_fail = sum(1 for it in items if it["reward"] == 0)

    print(f"Data      : {args.data}")
    if args.norms:
        print(f"Norms     : {args.norms}")
    print(f"Loaded    : {len(items)} traces")
    print(f"  Pass / Fail : {n_pass} / {n_fail}")
    print(f"  Norms seen  : {', '.join(all_norm_ids)}")
    if args.norm:
        print(f"  Filter      : norm={args.norm}")
    print(f"\nOpen: http://localhost:{args.port}")
    print("Keys: ← / → or p / n to navigate\n")

    app.run(debug=False, port=args.port)


if __name__ == "__main__":
    main()
